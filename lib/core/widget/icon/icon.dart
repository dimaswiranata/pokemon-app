import 'package:flutter/widgets.dart';
import 'package:nexus_app/core/index.dart';

class AntiIcon extends StatelessWidget {
  final IconType iconType;
  final dynamic iconName;
  final TextDirection? textDirection;
  final double? size;
  final double? fill;
  final double? weight;
  final double? grade;
  final double? opticalSize;
  final Color? color;
  final List<Shadow>? shadows;
  final String? semanticLabel;

  const AntiIcon({
    super.key,
    this.iconType = IconType.filled,
    this.iconName = IconName.commerce_barcode,
    this.textDirection,
    this.size,
    this.fill,
    this.weight,
    this.grade,
    this.opticalSize,
    this.color,
    this.shadows,
    this.semanticLabel,
  });

  @override
  Widget build(BuildContext context) {
    String newIconName = iconName is String ? iconName : IconNameExtension(iconName).key;
    switch (iconType) {
      case IconType.regular:
        return Icon(
          AntiIconLight.getIcon(newIconName),
          textDirection: textDirection,
          size: size,
          fill: fill,
          weight: weight,
          grade: grade,
          opticalSize: opticalSize,
          color: color,
          shadows: shadows,
          semanticLabel: semanticLabel,
        );
      case IconType.light:
        return Icon(
          AntiIconLight.getIcon(newIconName),
          textDirection: textDirection,
          size: size,
          fill: fill,
          weight: weight,
          grade: grade,
          opticalSize: opticalSize,
          color: color,
          shadows: shadows,
          semanticLabel: semanticLabel,
        );
      case IconType.app:
        return Icon(
          AntiIconApp.getIcon(newIconName),
          textDirection: textDirection,
          size: size,
          fill: fill,
          weight: weight,
          grade: grade,
          opticalSize: opticalSize,
          color: color,
          shadows: shadows,
          semanticLabel: semanticLabel,
        );
      default:
        return Icon(
          AntiIconFilled.getIcon(newIconName),
          textDirection: textDirection,
          size: size,
          fill: fill,
          weight: weight,
          grade: grade,
          opticalSize: opticalSize,
          color: color,
          shadows: shadows,
          semanticLabel: semanticLabel,
        );
    }
  }
}

